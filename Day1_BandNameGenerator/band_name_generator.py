"""
Band Name Generator Simple Project
Get the city name where user was born and nick name of user
Add those two strings together to form a band name
"""
print("Welcome to the Band Name Generator")
city_name = input("Enter the city where you were born:\n")
nick_name = input("Enter your nick name:\n")
band_name = city_name + " " + nick_name
print("Your band name will be: " + band_name)
